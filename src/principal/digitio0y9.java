public class digitio0y9 {
    /**
     * @.0<img src="SolucEj18MR.JPG">
     * <h1>Un subprograma que se encarga de comprobar si un número está entre 0 y 9</h1>
     * @author David
     * @param num Variable tipo entero
     * @return Devuelve si el número está o no entre 0 y 9
     */
    public static void DIGITO (int num){

         if(num>=0 && num<=9){

             System.out.println("Si está entre 0 y 9");

         }else{

             System.out.println("No está entre 0 y 9");
         }


    }
}


